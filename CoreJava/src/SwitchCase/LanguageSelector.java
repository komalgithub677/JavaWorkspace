package SwitchCase;

public class LanguageSelector {
             public static void main(String[] args) {
            	 //25. Language Selector

            	 //User selects:

            	 //English

            	 //Hindi

            	 //Marathi
            	 
            	 String language = "English";
            	 
            	 switch(language) {
            	 case "English":
            		 System.out.println("Language would be the english");
            	 break;
            	 
            	 case "Hindi":
            		 System.out.println("Language would be the Hindi");
            	 break;
            	 
            	 case "Marathi":
            		 System.out.println("Language would be the Marathi");
            	 break;
            	 
            	 default:
            		 System.out.println("Laguage would not be valid");
            	 }
            	 
			}
}
