package practice.Practice1;

public class Numbers1To100 {
             public void printNumbers() {
            	 for(int i=0; i<=100; i++ ) {
            		 System.out.println(i);
            	 }
             }
             
             public static void main(String[] args) {
				Numbers1To100 n = new Numbers1To100();
				n.printNumbers();
			}
}
